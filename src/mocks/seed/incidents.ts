function resident(uuid: string): Record<string, unknown> {
  const r = RESIDENT_NAMES[uuid];
  return {
    id: uuid,
    uuid,
    first_name: r.first,
    last_name: r.last,
    email: r.email,
    avatar_url: '',
    lead_uuid: r.lead,
    date_of_birth: r.dob,
    group_home: { id: Number(r.gh.split('-')[1]), name: r.ghName, address: { line1: `${100 + Number(r.gh.split('-')[1])} Main St` } },
  };
}

function reporter(first: string, last: string, role: string): Record<string, unknown> {
  return {
    uuid: `user-${first.toLowerCase()}`,
    email: `${first.toLowerCase()}.${last.toLowerCase()}@customgrouphome.com`,
    first_name: first,
    last_name: last,
    avatar_url: '',
    role,
  };
}

function signature(first: string, last: string, at: string): Record<string, unknown> {
  return {
    uploaded_at: at,
    updated_at: at,
    alt_text: 'signature_draw',
    uploaded_by: { id: 1, email: `${first.toLowerCase()}.${last.toLowerCase()}@customgrouphome.com`, first_name: first, last_name: last },
  };
}

